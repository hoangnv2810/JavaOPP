package BangDiemTheoMonHoc;

public class MonHoc {
    private String ma, ten;
    private int tinchi;

    public MonHoc() {
    }

    public MonHoc(String ma, String ten, int tinchi) {
        this.ma = ma;
        this.ten = ten;
        this.tinchi = tinchi;
    }

    public String getMa() {
        return ma;
    }

    public String getTen() {
        return ten;
    }
}
